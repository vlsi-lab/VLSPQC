uint32_t mq_montymul_sw(uint32_t x, uint32_t y) {
    uint32_t z, w;
    z = x * y;
    w = ((z * FALCON_Q0I) & 0xFFFF) * FALCON_Q;
    z = (z + w) >> 16;
    z -= FALCON_Q;
    z += FALCON_Q & -(z >> 31);
    return z;
}

