// Define the function once
void gf2_mul64_sw(uint64_t c[2], uint64_t a, uint64_t b) {
    uint64_t lo = 0, hi = 0;
    for (int k = 0; k < 64; ++k) {
        uint64_t mask = -((a >> k) & 1ULL);
        if (k == 0) lo ^= b & mask;
        else {
            lo ^= (b << k) & mask;
            hi ^= (b >> (64 - k)) & mask;
        }
    }
    c[0] = lo; c[1] = hi;
}


void gf_carryless_mul_sw(uint8_t *c, uint8_t a, uint8_t b) {
    uint16_t h = 0, l = 0, g, u[4];
    u[0] = 0;
    u[1] = b & 0x7F;
    u[2] = u[1] << 1;
    u[3] = u[2] ^ u[1];
  
    for (int i = 0; i < 8; i += 2) {
        g = 0;
        uint16_t tmp1 = (a >> i) & 3;
        for (int j = 0; j < 4; ++j) {
            uint32_t tmp2 = tmp1 - j;
            g ^= (u[j] & -(1 - ((tmp2 | -tmp2) >> 31)));
        }
        if (i == 0) l = g;
        else {
            l ^= (g << i);
            h ^= (g >> (8 - i));
        }
    }
    uint16_t mask = (-((b >> 7) & 1));
    l ^= ((a << 7) & mask);
    h ^= ((a >> 1) & mask);
    c[0] = (uint8_t)l;
    c[1] = (uint8_t)h;
}


